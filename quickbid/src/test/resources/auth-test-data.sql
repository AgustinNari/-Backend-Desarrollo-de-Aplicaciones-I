DROP ALL OBJECTS;

CREATE TABLE paises (
    numero integer PRIMARY KEY,
    nombre varchar(250) NOT NULL
);

CREATE TABLE personas (
    identificador integer GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    documento varchar(20) NOT NULL,
    nombre varchar(150) NOT NULL,
    direccion varchar(250),
    estado varchar(15)
);

CREATE TABLE clientes (
    identificador integer PRIMARY KEY,
    "numeroPais" integer,
    admitido varchar(2),
    categoria varchar(10),
    verificador integer NOT NULL
);

CREATE TABLE empleados (
    identificador integer PRIMARY KEY,
    cargo varchar(100),
    sector integer
);

CREATE TABLE seguros (
    "nroPoliza" varchar(30) PRIMARY KEY,
    compania varchar(150) NOT NULL,
    "polizaCombinada" varchar(2),
    importe decimal(18,2) NOT NULL
);

CREATE TABLE duenios (
    identificador integer PRIMARY KEY,
    "numeroPais" integer,
    "verificacionFinanciera" varchar(2),
    "verificacionJudicial" varchar(2),
    "calificacionRiesgo" integer,
    verificador integer NOT NULL
);

CREATE TABLE subastas (
    identificador integer GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    fecha date NOT NULL,
    hora time NOT NULL,
    estado varchar(10),
    ubicacion varchar(350),
    categoria varchar(10)
);

CREATE TABLE productos (
    identificador integer GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    fecha date,
    disponible varchar(2),
    "descripcionCatalogo" varchar(500),
    "descripcionCompleta" varchar(300),
    revisor integer,
    duenio integer,
    seguro varchar(30)
);

CREATE TABLE fotos (
    identificador integer GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    producto integer NOT NULL,
    foto binary varying NOT NULL
);

CREATE TABLE catalogos (
    identificador integer GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    descripcion varchar(250) NOT NULL,
    subasta integer,
    responsable integer
);

CREATE TABLE "itemsCatalogo" (
    identificador integer GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    catalogo integer NOT NULL,
    producto integer NOT NULL,
    "precioBase" decimal(18,2) NOT NULL,
    comision decimal(18,2) NOT NULL,
    subastado varchar(2)
);

CREATE TABLE asistentes (
    identificador integer GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    "numeroPostor" integer NOT NULL,
    cliente integer NOT NULL,
    subasta integer NOT NULL
);

CREATE TABLE pujos (
    identificador integer GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    asistente integer NOT NULL,
    item integer NOT NULL,
    importe decimal(18,2) NOT NULL,
    ganador varchar(2) DEFAULT 'no'
);

CREATE TABLE "registroDeSubasta" (
    identificador integer GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    subasta integer NOT NULL,
    duenio integer NOT NULL,
    producto integer NOT NULL,
    cliente integer NOT NULL,
    importe decimal(18,2) NOT NULL,
    comision decimal(18,2) NOT NULL
);

CREATE TABLE app_archivos (
    id bigint GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
